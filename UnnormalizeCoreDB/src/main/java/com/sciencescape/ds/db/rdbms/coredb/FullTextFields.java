package main.java.com.sciencescape.ds.db.rdbms.coredb;

public class FullTextFields {

	private String _abstract;
	private String _introduction;
	private String _literature;
	private String _experiments;
	private String _evaluation;
	private String _conclusion;
	private String _references;
	
	public String get_abstract() {
		return _abstract;
	}
	public void set_abstract(String _abstract) {
		this._abstract = _abstract;
	}
	public String get_introduction() {
		return _introduction;
	}
	public void set_introduction(String _introduction) {
		this._introduction = _introduction;
	}
	public String get_literature() {
		return _literature;
	}
	public void set_literature(String _literature) {
		this._literature = _literature;
	}
	public String get_experiments() {
		return _experiments;
	}
	public void set_experiments(String _experiments) {
		this._experiments = _experiments;
	}
	public String get_evaluation() {
		return _evaluation;
	}
	public void set_evaluation(String _evaluation) {
		this._evaluation = _evaluation;
	}
	public String get_conclusion() {
		return _conclusion;
	}
	public void set_conclusion(String _conclusion) {
		this._conclusion = _conclusion;
	}
	public String get_references() {
		return _references;
	}
	public void set_references(String _references) {
		this._references = _references;
	}
}
